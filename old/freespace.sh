then
	echo "freespace.sh displays the space free on a drive you specify"
	echo
	echo "USAGE"
	echo "  freespace.sh DRIVE"
	echo
	echo "ARGUMENTS"
	echo "  DRIVE - the drive to display free space for"
	echo
	exit
fi

df -h "$1" | awk '{print $4}' | grep -E -o '[0-9]+([\.][0-9]+)?[G|K|M]?'